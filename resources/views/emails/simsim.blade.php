    <p>Dear Employee ,</p>


    <p>GlowLogix uses Payroll Plus solution for salary disbursement. To enjoy the service, you need to install SimSim mobile
        app and get yourself registered on it.</p>


    <b>Here’s how to install and register on SimSim app:</b>

    <ul>
        <li>Download and install the SimSim Pakistan by FINJA App from Play Store or App Store.</li>

        <li>Click on the ‘Create Your SimSim Wallet’ button and enter your CNIC, Mobile Number, and carrier and click on ‘Submit.’</li>

        <li>On the next screen, enter your name, mother’s name and the city of birth and click ‘Submit’ again.</li>

        <li>Read the terms and conditions, once you’re done click ‘Accept.’ You’ll see a screen which will tell you the time
            taken to create your wallet. Click on ‘Next.’</li>

        <li>One the next screen, fill in the form by selecting your desired first name, father’s name, mailing address, and email
            ID. Click ‘Submit’ when done.</li>

        <li>You’ll be prompted to enter the OTP (verification number) sent to your mobile phone. Enter the OTP and click ‘Submit.’
            Your account has now been verified!</li>

        <li>On the next screen enter a 4-digit pin. Once generate the pin, you need to keep it confidential and use it while
            login to your account.</li>

        <li>After adding a profile picture click next and you will have reached your SimSim Wallet’s ‘Home’ Screen.</li>
    </ul>

